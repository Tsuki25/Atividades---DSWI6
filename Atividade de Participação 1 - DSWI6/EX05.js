5 == "5"    // => True
5 === "5"   // => False
5 == 5      // => True
5 === 5     // => True

console.log(5 == "5");
console.log(5 === "5");
console.log(5 == 5);
console.log(5 === 5);

// Acertei as opções, pois sabia que == representa igualdade de valor e === significa igualdade de valor e de tipo